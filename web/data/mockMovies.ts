export function getMoviesByMoods(moods: MoodTag[]): Movie[] {
  if (moods.length === 0) return mockMovies;

  return mockMovies.filter(movie =>
    moods.some(mood => movie.moodTags.includes(mood))
  ).sort((a, b) => {
    // Sort by number of matching moods
    const aMatches = moods.filter(mood => a.moodTags.includes(mood)).length;
    const bMatches = moods.filter(mood => b.moodTags.includes(mood)).length;
    return bMatches - aMatches;
  });
}

export function getMovieById(id: number): Movie | undefined {
  return mockMovies.find(movie => movie.id === id);
}
